// Content modules are lazy-loaded from ./content/<slug>.js
const contentModules = import.meta.glob('./content/*.js')

export async function loadArticleContent(slug) {
  const path = `./content/${slug}.js`
  if (contentModules[path]) {
    const mod = await contentModules[path]()
    return mod.default
  }
  return null
}

export function getArticleBySlug(slug) {
  return ARTICLES.find(a => a.slug === slug) || null
}

